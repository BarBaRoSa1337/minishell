#include "../minishell.h"

void	*ft_memcpy(void *dest, void *src, size_t n)
{
	size_t	i;
	char	*destination;
	char	*source;

	destination = dest;
	source = src;
	if (!dest && !src)
		return (0);
	if (destination == source)
		return (dest);
	i = 0;
	while (i < n)
	{
		((char *)dest)[i] = ((char *)src)[i];
		i++;
	}
	return (dest);
}
